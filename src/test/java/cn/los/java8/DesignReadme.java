package cn.los.java8;

public class DesignReadme {

    // 设计和架构原则
    // 软件开放最重要的设计工具不是什么技术，而是一颗在设计原则方面训练有素的头脑--Craing Larman
    // Lamdba 表达式没有什么特别的，和接口、类一样，它只是程序员工具箱中的一件新工具。
    // 使用Lamdba 表达式实现 SOLID 原则，该原则是开放良好面向对象程序的准则，使用Lamdba表达式，还能改进一些现有的设计模式
    // SOLID—— Single Responsibility、Open/closed、Liskov substitution、Interface
    // segregation、Dependency inversion (这些原则能指导你开发出，易于维护和扩展的代码)

    // SRP 单一职责原则 一个类有且只有一个更改的原因。
    // OCP 开闭原则 能够不更改类而扩展类的行为。
    // LSP 里氏替换原则 派生类可以替换基类被使用。
    // ISP 接口隔离原则 使用客户端特定的细粒度接口。
    // DIP 依赖反转原则 依赖抽象而不是具体实现。

}
